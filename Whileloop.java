package program;

/**
 * practical no-4 Write a program to demonstrate the use of while loop print 1
 * to 10
 *
 * @author Krushali
 */
public class Whileloop {

	public static void main(String[] args) {
		// declare variable
		int num = 1;

		// While loop from 1 to 10
		while (num <= 10) { //
			System.out.println(num);
			num = num + 1;
		}
	}

}
