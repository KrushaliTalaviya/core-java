package program;

/**
 * practical no-4 Write a program to demonstrate the use of do-while loop print
 * 1 to 10
 *
 * @author Krushali
 */
public class Dowhileloop {

	public static void main(String[] args) {
		// declare variable
		int num = 1;

		// do-while loop 1 to 10
		do {

			System.out.println(num);
			num++;
		} while (num <= 10);
	}

}
